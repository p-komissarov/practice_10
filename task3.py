N = int(input("Введите количество кварталов по вертикали (N): "))
M = int(input("Введите количество кварталов по горизонтали (M): "))
K = int(input("Введите количество кварталов, которое нужно отделить (K): "))

can_cut = (K % N == 0 and K // N < M) or (K % M == 0 and K // M < N)

if can_cut:
    print("Да, можно отделить ровно", K, "кварталов, перекрыв одну улицу.")
else:
    print("Нет, нельзя отделить ровно", K, "кварталов одним перекрытием.")